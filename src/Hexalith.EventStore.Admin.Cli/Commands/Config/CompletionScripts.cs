        _ = sb.AppendLine("                backup) compadd list trigger restore validate export-stream import-stream;;");
        _ = sb.AppendLine("            esac");
        _ = sb.AppendLine("            ;;");
        _ = sb.AppendLine("    esac");
        _ = sb.AppendLine("}");
        _ = sb.AppendLine();
        _ = sb.AppendLine("compdef _eventstore_admin eventstore-admin");
        return sb.ToString();
    }

    /// <summary>
    /// Generates a PowerShell completion script.
    /// </summary>
    public static string GeneratePowerShell() {
        StringBuilder sb = new();
        _ = sb.AppendLine($"# Generated by eventstore-admin config completion powershell on {DateTime.UtcNow:yyyy-MM-dd}");
        _ = sb.AppendLine("# Add to $PROFILE: Invoke-Expression (eventstore-admin config completion powershell | Out-String)");
        _ = sb.AppendLine();
        _ = sb.AppendLine("Register-ArgumentCompleter -CommandName eventstore-admin -ScriptBlock {");
        _ = sb.AppendLine("    param($wordToComplete, $commandAst, $cursorPosition)");
        _ = sb.AppendLine();
        _ = sb.AppendLine("    $commands = $commandAst.CommandElements | ForEach-Object { $_.ToString() }");
        _ = sb.AppendLine("    $subcommands = $commands | Select-Object -Skip 1 | Where-Object { $_ -notmatch '^-' }");
        _ = sb.AppendLine();
        _ = sb.AppendLine("    # Dynamic profile names");
        _ = sb.AppendLine("    function Get-Profiles {");
        _ = sb.AppendLine("        $profileFile = Join-Path $env:USERPROFILE '.eventstore' 'profiles.json'");
        _ = sb.AppendLine("        if (Test-Path $profileFile) {");
        _ = sb.AppendLine("            $json = Get-Content $profileFile | ConvertFrom-Json");
        _ = sb.AppendLine("            if ($json.profiles) {");
        _ = sb.AppendLine("                $json.profiles | Get-Member -MemberType NoteProperty | Select-Object -ExpandProperty Name");
        _ = sb.AppendLine("            }");
        _ = sb.AppendLine("        }");
        _ = sb.AppendLine("    }");
        _ = sb.AppendLine();
        _ = sb.AppendLine("    # Check if previous word is an option that expects a value");
        _ = sb.AppendLine("    $prevWord = if ($commands.Count -gt 1) { $commands[-2] } else { '' }");
        _ = sb.AppendLine("    switch ($prevWord) {");
        _ = sb.AppendLine("        { $_ -in '--format', '-f' } {");
        _ = sb.AppendLine("            'json', 'csv', 'table' | Where-Object { $_ -like \"$wordToComplete*\" } |");
        _ = sb.AppendLine("                ForEach-Object { [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', $_) }");
        _ = sb.AppendLine("            return");
        _ = sb.AppendLine("        }");
        _ = sb.AppendLine("        { $_ -in '--profile', '-p' } {");
        _ = sb.AppendLine("            Get-Profiles | Where-Object { $_ -like \"$wordToComplete*\" } |");
        _ = sb.AppendLine("                ForEach-Object { [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', $_) }");
        _ = sb.AppendLine("            return");
        _ = sb.AppendLine("        }");
        _ = sb.AppendLine("    }");
        _ = sb.AppendLine();
        _ = sb.AppendLine("    $completions = switch ($subcommands -join ' ') {");
        _ = sb.AppendLine($"        '' {{ '{Subcommands}'.Split() }}");
        _ = sb.AppendLine($"        'config' {{ '{ConfigSubcommands}'.Split() }}");
        _ = sb.AppendLine($"        'config profile' {{ '{ProfileSubcommands}'.Split() }}");
        _ = sb.AppendLine("        'config use' { @((Get-Profiles) + '--clear') }");
        _ = sb.AppendLine("        'config completion' { 'bash', 'zsh', 'powershell', 'fish' }");
        _ = sb.AppendLine("        'health' { 'dapr' }");
        _ = sb.AppendLine("        'stream' { 'list', 'events', 'event', 'state', 'diff', 'causation' }");
        _ = sb.AppendLine("        'projection' { 'list', 'status', 'pause', 'resume', 'reset' }");
        _ = sb.AppendLine("        'tenant' { 'list', 'detail', 'quotas', 'users', 'compare', 'verify' }");
        _ = sb.AppendLine("        'snapshot' { 'create', 'policies', 'set-policy', 'delete-policy' }");
        _ = sb.AppendLine("        'backup' { 'list', 'trigger', 'restore', 'validate', 'export-stream', 'import-stream' }");
        _ = sb.AppendLine("    }");
        _ = sb.AppendLine();
        _ = sb.AppendLine("    $completions | Where-Object { $_ -like \"$wordToComplete*\" } |");
        _ = sb.AppendLine("        ForEach-Object { [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', $_) }");
        _ = sb.AppendLine("}");
        return sb.ToString();
    }

    /// <summary>
    /// Generates a fish completion script.
    /// </summary>
    public static string GenerateFish() {
        StringBuilder sb = new();
        _ = sb.AppendLine($"# Generated by eventstore-admin config completion fish on {DateTime.UtcNow:yyyy-MM-dd}");
        _ = sb.AppendLine("# Add to ~/.config/fish/completions/eventstore-admin.fish or eval:");
        _ = sb.AppendLine("# eventstore-admin config completion fish | source");
        _ = sb.AppendLine();
        _ = sb.AppendLine("# Dynamic profile names");
        _ = sb.AppendLine("function __eventstore_profiles");
        _ = sb.AppendLine("    set -l profile_file \"$HOME/.eventstore/profiles.json\"");
        _ = sb.AppendLine("    if test -f \"$profile_file\"");
        _ = sb.AppendLine("        grep -o '\"[a-zA-Z0-9_-]*\"\\s*:' \"$profile_file\" | sed 's/[\":]//g; s/^\\s*//; s/\\s*//' | grep -v '^version$\\|^activeProfile$\\|^profiles$\\|^url$\\|^token$\\|^format$'");

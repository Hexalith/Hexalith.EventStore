using System.Text;

namespace Hexalith.EventStore.Admin.Cli.Commands.Config;

/// <summary>
/// Generates shell completion scripts for various shells.
/// </summary>
public static class CompletionScripts {
    private const string Subcommands = "health stream projection tenant snapshot backup config";
    private const string GlobalOptions = "--url --token --format --output --profile";
    private const string FormatValues = "json csv table";
    private const string ConfigSubcommands = "profile use current completion";
    private const string ProfileSubcommands = "add list show remove";

    /// <summary>
    /// Generates a bash completion script.
    /// </summary>
    public static string GenerateBash() {
        StringBuilder sb = new();
        _ = sb.AppendLine($"# Generated by eventstore-admin config completion bash on {DateTime.UtcNow:yyyy-MM-dd}");
        _ = sb.AppendLine("# Add to ~/.bashrc: eval \"$(eventstore-admin config completion bash)\"");
        _ = sb.AppendLine();
        _ = sb.AppendLine("_eventstore_admin_completions() {");
        _ = sb.AppendLine("    local cur prev words cword");
        _ = sb.AppendLine("    _init_completion || return");
        _ = sb.AppendLine();
        _ = sb.AppendLine("    # Dynamic profile names");
        _ = sb.AppendLine("    local profiles=\"\"");
        _ = sb.AppendLine("    local profile_file=\"${HOME}/.eventstore/profiles.json\"");
        _ = sb.AppendLine("    if [ -f \"${profile_file}\" ]; then");
        _ = sb.AppendLine("        profiles=$(grep -o '\"[a-zA-Z0-9_-]*\"\\s*:' \"${profile_file}\" | sed 's/[\":]//g; s/^\\s*//; s/\\s*$//' | grep -v '^version$\\|^activeProfile$\\|^profiles$\\|^url$\\|^token$\\|^format$')");
        _ = sb.AppendLine("    fi");
        _ = sb.AppendLine();
        _ = sb.AppendLine("    case \"${prev}\" in");
        _ = sb.AppendLine("        --format|-f)");
        _ = sb.AppendLine($"            COMPREPLY=( $(compgen -W \"{FormatValues}\" -- \"${{cur}}\") )");
        _ = sb.AppendLine("            return;;");
        _ = sb.AppendLine("        --profile|-p)");
        _ = sb.AppendLine("            COMPREPLY=( $(compgen -W \"${profiles}\" -- \"${cur}\") )");
        _ = sb.AppendLine("            return;;");
        _ = sb.AppendLine("        --output|-o|--url|-u|--token|-t)");
        _ = sb.AppendLine("            return;;");
        _ = sb.AppendLine("    esac");
        _ = sb.AppendLine();
        _ = sb.AppendLine("    local subcmd=\"\"");
        _ = sb.AppendLine("    for ((i=1; i < cword; i++)); do");
        _ = sb.AppendLine("        case \"${words[i]}\" in");
        _ = sb.AppendLine("            -*);;");
        _ = sb.AppendLine("            *) subcmd=\"${subcmd:+${subcmd} }${words[i]}\";;");
        _ = sb.AppendLine("        esac");
        _ = sb.AppendLine("    done");
        _ = sb.AppendLine();
        _ = sb.AppendLine("    case \"${subcmd}\" in");
        _ = sb.AppendLine("        \"\")");
        _ = sb.AppendLine($"            COMPREPLY=( $(compgen -W \"{Subcommands} {GlobalOptions}\" -- \"${{cur}}\") );;");
        _ = sb.AppendLine("        config)");
        _ = sb.AppendLine($"            COMPREPLY=( $(compgen -W \"{ConfigSubcommands}\" -- \"${{cur}}\") );;");
        _ = sb.AppendLine("        \"config profile\")");
        _ = sb.AppendLine($"            COMPREPLY=( $(compgen -W \"{ProfileSubcommands}\" -- \"${{cur}}\") );;");
        _ = sb.AppendLine("        \"config use\")");
        _ = sb.AppendLine("            COMPREPLY=( $(compgen -W \"${profiles} --clear\" -- \"${cur}\") );;");
        _ = sb.AppendLine("        \"config completion\")");
        _ = sb.AppendLine("            COMPREPLY=( $(compgen -W \"bash zsh powershell fish\" -- \"${cur}\") );;");
        _ = sb.AppendLine("        health)");
        _ = sb.AppendLine("            COMPREPLY=( $(compgen -W \"dapr --wait --interval --strict\" -- \"${cur}\") );;");
        _ = sb.AppendLine("        stream)");
        _ = sb.AppendLine("            COMPREPLY=( $(compgen -W \"list events event state diff causation\" -- \"${cur}\") );;");
        _ = sb.AppendLine("        projection)");
        _ = sb.AppendLine("            COMPREPLY=( $(compgen -W \"list status pause resume reset\" -- \"${cur}\") );;");
        _ = sb.AppendLine("        tenant)");
        _ = sb.AppendLine("            COMPREPLY=( $(compgen -W \"list detail quotas users compare verify\" -- \"${cur}\") );;");
        _ = sb.AppendLine("        snapshot)");
        _ = sb.AppendLine("            COMPREPLY=( $(compgen -W \"create policies set-policy delete-policy\" -- \"${cur}\") );;");
        _ = sb.AppendLine("        backup)");
        _ = sb.AppendLine("            COMPREPLY=( $(compgen -W \"list trigger restore validate export-stream import-stream\" -- \"${cur}\") );;");
        _ = sb.AppendLine("    esac");
        _ = sb.AppendLine("}");
        _ = sb.AppendLine();
        _ = sb.AppendLine("complete -F _eventstore_admin_completions eventstore-admin");
        return sb.ToString();
    }

    /// <summary>
    /// Generates a zsh completion script.
    /// </summary>
    public static string GenerateZsh() {
        StringBuilder sb = new();
        _ = sb.AppendLine($"# Generated by eventstore-admin config completion zsh on {DateTime.UtcNow:yyyy-MM-dd}");
        _ = sb.AppendLine("# Add to ~/.zshrc: eval \"$(eventstore-admin config completion zsh)\"");
        _ = sb.AppendLine();
        _ = sb.AppendLine("_eventstore_admin() {");
        _ = sb.AppendLine("    local -a subcommands profiles");
        _ = sb.AppendLine();
        _ = sb.AppendLine("    # Dynamic profile names");
        _ = sb.AppendLine("    _eventstore_profiles() {");
        _ = sb.AppendLine("        local profile_file=\"${HOME}/.eventstore/profiles.json\"");
        _ = sb.AppendLine("        if [ -f \"${profile_file}\" ]; then");
        _ = sb.AppendLine("            profiles=($(grep -o '\"[a-zA-Z0-9_-]*\"\\s*:' \"${profile_file}\" | sed 's/[\":]//g; s/^\\s*//; s/\\s*$//' | grep -v '^version$\\|^activeProfile$\\|^profiles$\\|^url$\\|^token$\\|^format$'))");
        _ = sb.AppendLine("        fi");
        _ = sb.AppendLine("    }");
        _ = sb.AppendLine();
        _ = sb.AppendLine("    _arguments -s \\");
        _ = sb.AppendLine("        '--url[Admin API base URL]:url:' \\");
        _ = sb.AppendLine("        '--token[JWT Bearer token or API key]:token:' \\");
        _ = sb.AppendLine("        '--format[Output format]:format:(json csv table)' \\");
        _ = sb.AppendLine("        '--output[Redirect output to file]:file:_files' \\");
        _ = sb.AppendLine("        '--profile[Connection profile name]:profile:{_eventstore_profiles; compadd -a profiles}' \\");
        _ = sb.AppendLine("        '1:command:(health stream projection tenant snapshot backup config)' \\");
        _ = sb.AppendLine("        '*::arg:->args'");
        _ = sb.AppendLine();
        _ = sb.AppendLine("    case $state in");
        _ = sb.AppendLine("        args)");
        _ = sb.AppendLine("            case ${words[1]} in");
        _ = sb.AppendLine("                config) compadd profile use current completion;;");
        _ = sb.AppendLine("                health) compadd dapr;;");
        _ = sb.AppendLine("                stream) compadd list events event state diff causation;;");
